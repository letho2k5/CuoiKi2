    public static int getSPQuantity(String masp) {
    for (int i = 0; i < ModelSP.getRowCount(); i++) {
        if (masp.equals(ModelSP.getValueAt(i, 1))) { 
            return (int) ModelSP.getValueAt(i, 4);  
        }
    }
    return -1;  // Return -1 or an appropriate value if not found
}

    private void banhang() {
    String masp = txtMasp.getText();
    int soLuongBan;
    try {
        soLuongBan = Integer.parseInt(txtSolg.getText());
    } catch (NumberFormatException e) {
        System.out.println("So luong ban khong hop le.");
        return;
    }

    sanpham currentsp = dao.getProductById(masp);
    if (currentsp != null) {
        int soLuongHienTai = currentsp.getSoluong();
        if (soLuongHienTai >= 0) {
            int soLuongConLai = soLuongHienTai - soLuongBan;
            if (soLuongBan > soLuongHienTai) {
                JOptionPane.showMessageDialog(rootPane, "Hàng không đủ số lượng để bán. Vui lòng nhập thêm hàng!");
                jPanel3.setVisible(true);
                jPanel1.setVisible(false);
                btnupdateslg.setVisible(true);
                
            }
            if (soLuongConLai >= 0) {
                dao.updateSPQuantity(masp, soLuongConLai);
                JOptionPane.showMessageDialog(this, "Đặt hàng thành công!");
                System.out.println("Dat hang thanh cong!");
                jPanel1.setVisible(true);
        String DateOrder = "";
        int gia = 0;
        int soluong=0;
        String TKH = "";
        String Diachi = "";
        ComboxKind.setSelectedIndex(0);
        int SĐT = 0;
        int MaHD = 0;
       try {
        TKH = txtTKH.getText(); // Lấy tên khách hàng từ ô văn bản
        Diachi = txtDiachi.getText(); // Lấy địa chỉ từ ô văn bản
        SĐT = Integer.parseInt(txtSĐT.getText()); // Lấy số điện thoại từ ô văn bản
        soluong=Integer.parseInt(txtSolg.getText());
        gia=Integer.parseInt(txtGia.getText());
        gia=soluong*gia;

        DateOrder = txtDateOrder.getText();
        Hoadon hd = new Hoadon(MaHD, TKH, SĐT, Diachi, DateOrder, gia);
        new DAOhd().AddHD(hd);
        showTableHD();
        ServerConnection.sendspInfoToServer(hd);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Đặt hàng không thành công! Vui lòng kiểm tra thông tin nhập vào.");
            e.printStackTrace(); // You can remove this line in production, it's for debugging purposes.
        }

       String Masp="";
        
        Masp=txtMasp.getText();
        soluong=Integer.parseInt(txtSolg.getText());
        gia=Integer.parseInt(txtGia.getText());
        int Gia=soluong*gia;
        SanphamCT sp=new SanphamCT(Masp, soluong, Gia);
        new DAOspCT().AddSP(sp);
        showTableSPCT();

//        banhang();
        showTableSP();
            } else {
                System.out.println("Khong du so luong de ban.");
            }
        } else {
            System.out.println("Ma san pham khong duoc tim thay.");
        }
    } else {
        System.out.println("Khong tim thay san pham hien tai.");

    }
}


