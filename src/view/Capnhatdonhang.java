    private void showTableHD() {
        hoadon = new DAOhd().getListHD();
        ModelHD.setRowCount(0);
        for (Hoadon hd:hoadon) {
            ModelHD.addRow(new Object[]{
                TableHoadon.getRowCount()+1,hd.getTKH(),hd.getSĐT(),hd.getDiachi(),hd.getDateOrder(),hd.getThanhtien()
            });
        }
    }   

    private void Tong() {
        int sum = 0;
        int rowCount = TableHoadon.getRowCount();
        int columnIndex = 5; // Cột thứ 6 (chỉ số bắt đầu từ 0)

        for (int i = 0; i < rowCount; i++) {
            Object value = TableHoadon.getValueAt(i, columnIndex);
            if (value != null) {
                try {
                    sum += Integer.parseInt(value.toString());
                    
                } catch (NumberFormatException ex) {
                    // Xử lý lỗi nếu giá trị không phải là số nguyên
                    System.out.println("Gia tri khong hop le o don thu " + (i + 1));
                }
            }
        }
        
        // Hiển thị tổng
        tong.setText("Tổng doanh thu: " + sum + " VNĐ");
    }

    private void showTableSP() {
       Sanpham=new DAOsp().getListSP();
       ModelSP.setRowCount(0);
       for(sanpham s:Sanpham) {
           ModelSP.addRow(new Object[]{
               TableSP.getRowCount()+1,s.getMasp(),s.getTensp(),s.getLoai(),s.getSoluong(),s.getGia(),s.getHinhAnh()
           });
       }
    }

