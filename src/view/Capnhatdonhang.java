    private void btnupdateslgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateslgActionPerformed
        // TODO add your handling code here:
        int selectedIndex = TableSP.getSelectedRow();
    if (selectedIndex != -1) {
        String masp = TableSP.getValueAt(selectedIndex, 1).toString(); // Lấy mã sản phẩm từ hàng được chọn
        String newQuantityStr = JOptionPane.showInputDialog(this, "Nhập số lượng mới:");
        if (newQuantityStr != null && !newQuantityStr.isEmpty()) {
            try {
                int newQuantity = Integer.parseInt(newQuantityStr);
                if (newQuantity >= 0) {
                    DAOsp dao = new DAOsp(); // Tạo đối tượng DAOsp
                    dao.update1SPQuantity(masp, newQuantity); // Cập nhật số lượng vào cơ sở dữ liệu
                    JOptionPane.showMessageDialog(this, "Cập nhật số lượng thành công!");
                    showTableSP(); // Cập nhật lại bảng sau khi đã cập nhật dữ liệu
                    // Cập nhật lại số lượng trên bảng
                    TableSP.setValueAt(newQuantity, selectedIndex, 4); // Giả sử cột số lượng là cột thứ 5 (index 4)
                } else {
                    JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng là số nguyên dương.");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng là số nguyên dương.");
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn một hàng để cập nhật số lượng.");
    }
    }//GEN-LAST:event_btnupdateslgActionPerformed

