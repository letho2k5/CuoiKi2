    private void TableSPCTMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableSPCTMouseClicked
    int currentIndex = TableSPCT.getSelectedRow();
    if (currentIndex == lastSelectedIndex) {
        clickCount++;
    } else {
        clickCount = 1; // Reset số lần nhấp khi chọn hàng khác
        lastSelectedIndex = currentIndex;
    }

    if (clickCount == 2) {
        SanphamCT spct = sanphamct.get(currentIndex);
        new DAOspCT().DeleteSPCT(spct.getID());
        showTableSPCT();
        clickCount = 0; // Reset số lần nhấp sau khi xóa
        lastSelectedIndex = -1; // Reset chỉ số hàng đã chọn
    }
    }//GEN-LAST:event_TableSPCTMouseClicked

